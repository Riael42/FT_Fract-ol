#include "fractol.h"

void	ft_putstr(char *str)
{
	int	i;
	int	j;

	if (str == (void *)0)
		return ;
	i = 0;
	while (str[i] != '\0')
		i += 1;
	j = write(1, str, i);
	j += 1;
}
