#include "fractol.h"

int	ft_strncmp(char *s1, char *s2, int n)
{
	int	i;

	i = 0;
	if (s1 == (void *)0 || s2 == (void *)0 || n <= 0)
		return (424242);
	while (s1[i] == s2[i] && s1[i] != '\0')
	{
		i += 1;
		n -= 1;
	}
	return (s1[i] - s2[i]);
}
